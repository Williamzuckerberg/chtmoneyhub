bool GetFirstHDSerial_9X_Ports(char *buf, size_t len)
{
	bool done = false;
	HANDLE hPortIOCTL = 0;
	RaisePriorityClass rcls;
	// 先试图打开
	char pathbuf[MAX_PATH];
	wsprintf(pathbuf,"\\\\.\\%s",PORT_DRIVER_NAME);
	hPortIOCTL = CreateFile (pathbuf,0, 0, NULL,CREATE_NEW, FILE_FLAG_DELETE_ON_CLOSE, NULL);	
	if (hPortIOCTL == INVALID_HANDLE_VALUE) {
		GetSystemDirectory(pathbuf,MAX_PATH);
		// 这里如果 SYSDIR 是根目录会有问题，不过谁那么变态阿。。
		lstrcat(pathbuf,"\\IDEHDIO.VXD");
		HANDLE hFile = CreateFile(pathbuf,GENERIC_READ | GENERIC_WRITE,0,NULL,CREATE_ALWAYS,FILE_ATTRIBUTE_NORMAL,NULL);
		if (hFile == INVALID_HANDLE_VALUE)
			return false;
		DWORD cbWriteBytes;
		if (!WriteFile(hFile,PORT_DRIVER_DATA,PORT_DRIVER_SIZE,&cbWriteBytes,NULL)
			|| cbWriteBytes != PORT_DRIVER_SIZE )
		{
			CloseHandle(hFile);	
			DeleteFile(pathbuf);
			return false;
		}
		CloseHandle(hFile);
		hPortIOCTL = CreateFile ("\\\\.\\IDEHDIO.VXD",0, 0, NULL,CREATE_NEW, FILE_FLAG_DELETE_ON_CLOSE, NULL);	
		if (hPortIOCTL == INVALID_HANDLE_VALUE)
			// 还是失败那谁也搞不定了。。。
			return false;
	}

	DWORD cbBytesReturned = 0;
	pt_IdeDInfo pOutBufVxD = (pt_IdeDInfo)HeapAlloc(GetProcessHeap(),HEAP_ZERO_MEMORY,sizeof(rt_IdeDInfo));;
	if (pOutBufVxD) {
		DeviceIoControl (hPortIOCTL, m_cVxDFunctionIdesDInfo,
			0, 0, pOutBufVxD, sizeof(pt_IdeDInfo), &cbBytesReturned, 0);
		CloseHandle (hPortIOCTL);
		unsigned long int i = 0;
		for (i=0; i<8 && !done; i++)
		{
			if((pOutBufVxD->DiskExists[i]) && (pOutBufVxD->IDEExists[i/2]))
			{
				DWORD diskinfo [256];
				for (int j = 0; j < 256; j++) 
					diskinfo [j] = pOutBufVxD -> DisksRawInfo [i * 256 + j];
				GenSerial(diskinfo,buf,len);
				if (IsCharAlphaNumeric(buf[0])) {
					done = true;
				}
			}
		}
		HeapFree(GetProcessHeap(),0,pOutBufVxD);
	}
	return done;
}

