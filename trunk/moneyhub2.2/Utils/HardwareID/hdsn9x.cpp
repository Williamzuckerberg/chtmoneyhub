static void ChangeByteOrder( char *pStr, int size)
{
	for (int i=0;i<size-1;i+=2)
	{
		char temp = pStr[i];
		pStr[i] = pStr[i+1];
		pStr[i+1] = temp;
	}
}
bool GetFirstHDSerial_9X_SMART(char *buf, size_t len)
{
	bool done = false;
	int drive = 0;
	HANDLE hSmartDriveIOCTL = 0;
	
	hSmartDriveIOCTL = CreateFileA ("\\\\.\\SMARTVSD",0, 0, NULL,CREATE_NEW, 0, NULL);	
	if (hSmartDriveIOCTL == INVALID_HANDLE_VALUE) {
		// 弱智的 microsoft , ft, 98 默认安装把这个文件放错地方了。。。
		// 把 SYSDIR\smartvsd.vxd 拷贝到 SYSDIR\IOSUBSYS 下面去等待下次重起好了
		char *sysbuf = (char *)HeapAlloc(GetProcessHeap(),HEAP_ZERO_MEMORY,MAX_PATH);
		char *sysiosubsysbuf = (char *)HeapAlloc(GetProcessHeap(),HEAP_ZERO_MEMORY,MAX_PATH);
		if (sysbuf && sysiosubsysbuf) {
			GetSystemDirectoryA(sysbuf,MAX_PATH);
			GetSystemDirectoryA(sysiosubsysbuf,MAX_PATH);
			// 这里如果 SYSDIR 是根目录会有问题，不过谁那么变态阿。。
			lstrcatA(sysbuf,"\\SMARTVSD.VXD");
			lstrcatA(sysiosubsysbuf,"\\IOSUBSYS\\SMARTVSD.VXD");
			CopyFileA(sysbuf,sysiosubsysbuf,FALSE);
		}
		if (sysbuf)
			HeapFree(GetProcessHeap(),0,sysbuf);
		if (sysiosubsysbuf)
			HeapFree(GetProcessHeap(),0,sysiosubsysbuf);
		return false;
	}

	TUOTUO_SENDCMDINPARAMS *pscip = (TUOTUO_SENDCMDINPARAMS *)HeapAlloc(GetProcessHeap(),HEAP_ZERO_MEMORY,sizeof(TUOTUO_SENDCMDINPARAMS));
	BYTE *pIdOutCmd = (BYTE*) HeapAlloc(GetProcessHeap(),HEAP_ZERO_MEMORY,sizeof (TUOTUO_SENDCMDOUTPARAMS) + IDENTIFY_BUFFER_SIZE - 1);
	if (pscip && pIdOutCmd) 
		for (drive = 0; drive < MAX_IDE_DRIVES && !done ; drive ++)
		{
			ZeroMemory(pscip, sizeof(TUOTUO_SENDCMDINPARAMS));
			DWORD bytesReturned = 0;
			ZeroMemory(pIdOutCmd,  sizeof (TUOTUO_SENDCMDOUTPARAMS) + IDENTIFY_BUFFER_SIZE - 1);
			pscip->cBufferSize = IDENTIFY_BUFFER_SIZE;
			pscip->bDriveNumber = (BYTE) drive;
			pscip->irDriveRegs.bFeaturesReg = 0;
			pscip->irDriveRegs.bSectorCountReg = 1;
			pscip->irDriveRegs.bSectorNumberReg = 1;
			pscip->irDriveRegs.bCylLowReg = 0;
			pscip->irDriveRegs.bCylHighReg= 0;
			pscip->irDriveRegs.bDriveHeadReg = 0xa0;
			pscip->irDriveRegs.bCommandReg = 0xec;
			if (!DeviceIoControl(hSmartDriveIOCTL, DFP_RECEIVE_DRIVE_DATA, pscip, sizeof(TUOTUO_SENDCMDINPARAMS)-1 , pIdOutCmd, W9xBufferSize, &bytesReturned,NULL)){
				continue;
			}
			
			IDSECTOR *pIdSector = (IDSECTOR *)
				((PTUOTUO_SENDCMDOUTPARAMS) pIdOutCmd) -> bBuffer;
			
			ChangeByteOrder(pIdSector->sSerialNumber,sizeof(pIdSector->sSerialNumber));
			ZeroMemory(buf,len);
			MoveMemory(buf,pIdSector->sSerialNumber,sizeof(pIdSector->sSerialNumber));
			TrimSerial(buf)				;
			if (IsCharAlphaNumeric(buf[0])) {
				done = true;
			} 
		}
	if (pscip)
		HeapFree(GetProcessHeap(),0,pscip);
	if (pIdOutCmd)
		HeapFree(GetProcessHeap(),0,pIdOutCmd);
	CloseHandle(hSmartDriveIOCTL);	
	return done;	
}


